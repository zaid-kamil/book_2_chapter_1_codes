// Checking power levels
void main() {
  String character = 'Naruto';
  int powerLevel = 9000;

  if (character == 'Goku') {
    print('Goku is a powerful hero!');
  } else if (character == 'Naruto') {
    print('Naruto has a power level of $powerLevel!');
  } else if (character == 'Saitama') {
    print('Saitama is the most powerful hero!');
  }
  else {
    print('Other characters in the anime.');
  }
}
